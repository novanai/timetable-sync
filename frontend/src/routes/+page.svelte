<div class="m-4">
    <h1 class="text-primary-800 text-2xl font-semibold mb-2">Add to Google Calendar</h1>
    <ol class="list-decimal ml-6 mb-3">
        <li>On your computer, open <a class="text-primary-700" href="https://calendar.google.com/">Google Calendar</a></li>
        <li>Go to Settings &gt; Add Calendar &gt; From URL</li>
        <li>Enter the calendar&#39;s address, which you can <a class="text-primary-700" href="/generator">generate here</a></li>
    </ol>
    <p class="mb-3">To make sure you calendar syncs on mobile devices:</p>
    <ul class="list-disc ml-6">
        <li>On Android devices: open Google Calendar, go to settings, select the calendar and enable the &quot;Sync&quot; option</li>
        <li>
            On Apple devices: go to
            <a class="text-primary-700" href="https://calendar.google.com/calendar/syncselect">
                https://calendar.google.com/calendar/syncselect
            </a>,
            select the calendar(s) you want to sync, and then save
        </li>
    </ul>
    <br>

    <h1 class="text-primary-800 text-2xl font-semibold mb-2">Add to Apple Calendar</h1>
    <ol class="list-decimal ml-6 mb-3">
        <li>On your Apple device, open the Calendar app</li>
        <li>Go to Calendars &gt; Add Calendar &gt; Add Subscription Calendar</li>
        <li>
            Enter the calendar&#39;s address, which you can <a class="text-primary-700" href="/generator">generate here</a>,
            and then click &quot;Subscribe&quot;
        </li>
    </ol>
    <br>
    
    <h1 class="text-primary-800 text-2xl font-semibold mb-2">Download an iCal file</h1>
    <p>
        If you want to download an iCal file, just go to the calendar&#39;s address, which you can
        <a class="text-primary-700" href="/generator">generate here</a>, and download the generated
        iCal file when prompted.
    </p>
</div>
